# métodos

nome_curso = ' Edição de Video '

# todo testo maiuscula
print(nome_curso.upper())

#todo texto minusculo 
print(nome_curso.lower())

#retira espaco em branco dfe todos os lados
print(nome_curso.strip())

#retira espcao em branco da esquerda
print(nome_curso.lstrip())

#retira espcao em branco da direita
print(nome_curso.rstrip())

#verifica indice começa a letra que está procurando
print(nome_curso.find("ção"))

##substituir a 1 palavra com a 2
print(nome_curso.replace('Video', "Musica"))